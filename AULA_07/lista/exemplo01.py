# Lista
list_mes = ["Janeiro", "Fevereiro", "Março", "Abril", "Maio", "Junho", "Julho"]

# exibir a lista
print(list_mes)

# exibir primeiro item da lista
print(list_mes[0])

# exibir o último item da lista
print(list_mes[-1])

# exibir somente um parte da lista
# Intervalos são abertos. 
# Para que eu inclua 2, por exemplo, nublado e chuvoso
# 1 a 2...de 1 a 3
print(list_mes[1:3])

# exibir da posição 2 até o final
print(list_mes[2:])

# exibir de tempestade, posição 3 para o início
print(list_mes[:3])

# exibir elementos de ensolarado até tempestado, pulando de 1 e 1
print(list_mes[0:3:1])
